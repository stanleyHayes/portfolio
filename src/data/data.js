export const getLessonsByCourse = (slug) => {
    const course = getCourses().find(course => course.slug === slug);
    if (course) {
        return course.lessons
    }
    return null;
}

export const getCourseBySlug = slug => {
    const course = getCourses().find(course => course.slug === slug);
    if (course) {
        return course
    }
    return null;
}

export const getCourseLessonBySlug = (cslug, lslug) => {
    const lessons = getLessonsByCourse(cslug);
    const course = getCourseBySlug(cslug);
    let lesson = null;
    if(lessons){
        lesson = lessons.find(lesson => lesson.slug === lslug);
    }
    if(lesson && course){
        return [course, lessons, lesson];
    }
    return null;
}